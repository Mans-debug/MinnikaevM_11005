public class Customer
{
    private String name;
    private int id;
    private String sex;
    private int age;

   public Customer(String name, int id, String sex, int age)
    {
        this.name = name;
        this. id = id;
        this.sex = sex;
        this.age = age;

    }

    public String getName()
    {
        return name;
    }
}
